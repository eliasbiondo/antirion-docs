def epic_frontmatter(epic: Dict[str, Any]) -> Dict[str, Any]:
    return {
        "id": map_id(epic["id"]),
        "type": "epic",
        "title": epic["title"],
        "lifecycle": epic.get("lifecycle", "active"),
        "created": MIGRATION_DATE,
        "updated": MIGRATION_DATE,
        "release": PRIORITY_TO_RELEASE.get(epic.get("priority"), "backlog"),
        "order": order_from_old_id(epic["id"]),
        "owners": [],
        "tags": [],
    }


def _split_internal_external(
    refs: Optional[List[str]],
    in_scope: set,
) -> Tuple[List[str], List[str]]:
    """Return (internal, external) — refs that resolve in-scope vs those that
    point outside the migration set (will reconnect in pass 2)."""
    if not refs:
        return [], []
    internal: List[str] = []
    external: List[str] = []
    for r in refs:
        if r in in_scope:
            internal.append(r)
        else:
            external.append(r)
    return internal, external


def feature_frontmatter(
    feature: Dict[str, Any],
    dropped_self_edges: List[str],
    in_scope: set,
    external_refs: Dict[str, List[str]],
) -> Dict[str, Any]:
    fm: Dict[str, Any] = {
        "id": map_id(feature["id"]),
        "type": "feature",
        "title": feature["title"],
        "lifecycle": feature.get("lifecycle", "active"),
        "created": MIGRATION_DATE,
        "updated": MIGRATION_DATE,
        "parent": map_id(feature["epic"]),
        "release": PRIORITY_TO_RELEASE.get(feature.get("priority"), "backlog"),
        "order": order_from_old_id(feature["id"]),
        "status": initial_status_block(),
        "deployment_modes": ["saas", "self_hosted"],
        "installed_on": "tenant_install",
        "owners": [],
        "tags": [],
    }
    deps_new: List[str] = []
    for d in feature.get("depends_on") or []:
        if d == feature["id"]:
            dropped_self_edges.append(feature["id"])
            continue
        deps_new.append(map_id(d))
    internal, external = _split_internal_external(deps_new, in_scope)
    if internal:
        fm["depends_on"] = internal
    if external:
        external_refs.setdefault("depends_on", []).extend(external)
    for key in ("related", "supersedes"):
        v = feature.get(key)
        if v:
            ints, exts = _split_internal_external([map_id(x) for x in v], in_scope)
            if ints:
                fm[key] = ints
            if exts:
                external_refs.setdefault(key, []).extend(exts)
    if feature.get("superseded_by"):
        sb = map_id(feature["superseded_by"])
        if sb in in_scope:
            fm["superseded_by"] = sb
        else:
            external_refs.setdefault("superseded_by", []).append(sb)
    if feature.get("also_under"):
        ints, exts = _split_internal_external(map_id_list(feature["also_under"]), in_scope)
        if ints:
            fm["also_under"] = ints
        if exts:
            external_refs.setdefault("also_under", []).extend(exts)
    if feature.get("ui_references"):
        fm["ui_references"] = list(feature["ui_references"])
    if feature.get("external"):
        fm["external"] = feature["external"]
    return fm


