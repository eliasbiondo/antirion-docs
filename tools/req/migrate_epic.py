GENERATED_BLOCK_START = "<!-- generated:children:start -->"
GENERATED_BLOCK_END = "<!-- generated:children:end -->"


def build_epic_body(epic: Dict[str, Any]) -> str:
    title = epic["title"]
    description = epic.get("description", "").strip() or "_(no description in source YAML)_"
    return (
        f"# {title}\n\n"
        f"## Summary\n\n{description}\n\n"
        f"## Features\n\n{GENERATED_BLOCK_START}\n_(regenerated by `req index`)_\n{GENERATED_BLOCK_END}\n\n"
        f"## Notes\n\n_None._\n"
    )


def build_feature_body(
    feature: Dict[str, Any],
    external_refs: Optional[Dict[str, List[str]]] = None,
    dropped_self_edges: Optional[List[str]] = None,
) -> str:
    title = feature["title"]
    description = feature.get("description", "").strip() or "_(no description in source YAML)_"
    extra_notes: List[str] = []
    for k in ("mode",):
        if feature.get(k):
            extra_notes.append(f"- **{k}** (from source YAML): `{feature[k]}`")
    if dropped_self_edges:
        extra_notes.append(
            "- **Migration note:** dropped self-edge in `depends_on` "
            f"(`{', '.join(dropped_self_edges)}`); flagged for manual review."
        )
    if external_refs:
        extra_notes.append(_format_external_block(
            "External cross-epic references (will move to frontmatter once their epic is migrated in pass 2)",
            external_refs,
        ))
    notes = "\n".join(extra_notes) if extra_notes else "_None._"
    return (
        f"# {title}\n\n"
        f"## Summary\n\n{description}\n\n"
        f"## Stories\n\n{GENERATED_BLOCK_START}\n_(regenerated by `req index`)_\n{GENERATED_BLOCK_END}\n\n"
        f"## Notes\n\n{notes}\n"
    )


def render_bullets(items: Optional[List[Any]]) -> str:
    if not items:
        return "_None._"
    out = []
    for it in items:
        if isinstance(it, str):
            out.append(f"- {it}")
        elif isinstance(it, dict):
            # Render generic dicts as inline yaml under a bullet
            out.append("- " + yaml_dump(it).strip().replace("\n", "\n  "))
        else:
            out.append(f"- {it}")
    return "\n".join(out)


def render_api(api: Optional[List[Any]]) -> str:
    if not api:
        return "_None._"
    out: List[str] = []
    for entry in api:
        if not isinstance(entry, dict):
            out.append(f"- {entry}")
            continue
        method = entry.get("method", "")
        path = entry.get("path", "")
        head = f"### `{method} {path}`".strip()
        if head == "### ``":
            head = "### (unspecified endpoint)"
        out.append(head)
        notes = entry.get("notes")
        if notes:
            out.append(notes)
        for sub in ("request", "response", "errors"):
            sv = entry.get(sub)
            if sv is None:
                continue
            out.append(f"**{sub.capitalize()}:**")
            out.append("")
            out.append("```yaml")
            out.append(yaml_dump({sub: sv}).rstrip())
            out.append("```")
        if entry.get("inferred"):
            out.append("_inferred: true (carried over from source YAML)_")
        out.append("")
    return "\n".join(out).rstrip() + "\n"


def render_ui(ui: Optional[Dict[str, Any]]) -> str:
    if not ui:
        return "_None._"
    if not isinstance(ui, dict):
        return f"```yaml\n{yaml_dump({'ui': ui}).rstrip()}\n```"
    parts: List[str] = []
    for label, key in [("Screens", "screens"), ("Components", "components"), ("States", "states")]:
        v = ui.get(key)
        if v:
            parts.append(f"**{label}:**\n")
            parts.append(render_bullets(v))
            parts.append("")
    extra = {k: v for k, v in ui.items() if k not in ("screens", "components", "states")}
    if extra:
        parts.append("**Other UI metadata (preserved from source YAML):**\n")
        parts.append("```yaml")
        parts.append(yaml_dump(extra).rstrip())
        parts.append("```")
    return "\n".join(parts).rstrip() + "\n" if parts else "_None._"


def build_acceptance_section(acs: Optional[List[Any]]) -> str:
    if not acs:
        return "_None defined in source YAML._"
    out: List[str] = []
    for ac in acs:
        if not isinstance(ac, dict):
            continue
        ac_id = ac.get("id", "AC-???")
        scenario = ac.get("scenario", "(no scenario)")
        out.append(f"### {ac_id} — {scenario}")
        out.append("")
        for label, key in [("Given", "given"), ("When", "when"), ("Then", "then")]:
            vals = ac.get(key) or []
            out.append(f"**{label}:**")
            out.append("")
            for v in vals:
                out.append(f"- {v}")
            if not vals:
                out.append("- _(none specified)_")
            out.append("")
        # Capture any extra ac fields verbatim (e.g. notes)
        extra = {k: v for k, v in ac.items() if k not in ("id", "scenario", "given", "when", "then")}
        if extra:
            out.append("```yaml")
            out.append(yaml_dump(extra).rstrip())
            out.append("```")
            out.append("")
    return "\n".join(out).rstrip() + "\n"


def _format_external_block(label: str, external_refs: Dict[str, List[str]]) -> str:
    if not external_refs:
        return ""
    lines = [f"\n**{label}:**\n"]
    for key in sorted(external_refs):
        vals = sorted(set(external_refs[key]))
        lines.append(f"- `{key}`: {', '.join(f'`{v}`' for v in vals)}")
    return "\n".join(lines)


def build_story_body(
    story: Dict[str, Any],
    dropped_self_edges: List[str],
    external_refs: Optional[Dict[str, List[str]]] = None,
) -> str:
    title = story["title"]
    nar = story.get("narrative") or {}
    as_a = nar.get("as_a", "")
    iw = nar.get("i_want", "")
    st = nar.get("so_that", "")
    narrative_line = f"As a {as_a}, I want {iw} so that {st}."
    ac_block = build_acceptance_section(story.get("acceptance_criteria"))
    api_block = render_api(story.get("api"))
    ui_block = render_ui(story.get("ui"))
    dm_block = render_bullets(story.get("data_models"))
    nf_block = render_bullets(story.get("non_functional"))
    notes_lines: List[str] = []
    if story.get("audiences"):
        notes_lines.append("**Audiences (from source YAML):** " + ", ".join(story["audiences"]))
    if story.get("notes"):
        if isinstance(story["notes"], list):
            for n in story["notes"]:
                notes_lines.append(f"- {n}")
        else:
            notes_lines.append(str(story["notes"]))
    # Preserve any extra unmapped keys verbatim
    known = {"id", "feature", "title", "narrative", "priority", "acceptance_criteria",
             "api", "ui", "data_models", "non_functional", "depends_on", "related",
             "supersedes", "superseded_by", "audiences", "also_under", "lifecycle", "notes"}
    extras = {k: v for k, v in story.items() if k not in known}
    if extras:
        notes_lines.append("\n**Additional metadata preserved from source YAML:**\n")
        notes_lines.append("```yaml")
        notes_lines.append(yaml_dump(extras).rstrip())
        notes_lines.append("```")
    if dropped_self_edges:
        notes_lines.append("\n**Migration note:** the source YAML had a self-edge in `depends_on` "
                           "(`" + ", ".join(dropped_self_edges) + "`). It was dropped during migration "
                           "to preserve DAG validity; flagged for manual review.")
    if external_refs:
        notes_lines.append(_format_external_block(
            "External cross-epic references (will move to frontmatter once their epic is migrated in pass 2)",
            external_refs,
        ))
    notes_block = "\n".join(notes_lines).strip() or "_None._"
    return (
        f"# {title}\n\n"
        f"## Narrative\n\n{narrative_line}\n\n"
        f"## Acceptance Criteria\n\n{ac_block}\n"
        f"## API\n\n{api_block}\n"
        f"## UI\n\n{ui_block}\n"
        f"## Data Models\n\n{dm_block}\n\n"
        f"## Non-Functional Requirements\n\n{nf_block}\n\n"
        f"## Notes\n\n{notes_block}\n\n"
        f"## Open Questions\n\n_None._\n"
    )


