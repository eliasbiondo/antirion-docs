#!/usr/bin/env python3
"""
Migrate one epic from docs/requirements.yaml to the nested tree under
docs/requirements/. Idempotent: re-running on the same epic regenerates
the same files byte-for-byte (modulo the `updated:` field, which is held
constant at the migration date defined in the brief).

Usage:
    python tools/req/migrate_epic.py EPIC-008
    python tools/req/migrate_epic.py --all          # migrates every epic
    python tools/req/migrate_epic.py --dry-run EPIC-008

The script is the helper called for both pass 1 (EPIC-008 only) and pass 2
(every other epic). Decisions encoded here are the ones locked in the brief:
- ID mapping: EPIC-NNN → gw_epic_<NNN>, FEAT-NNN → gw_feature_<NNN>, STORY-NNN → gw_story_<NNN>
- Slug: kebab-cased title, ≤60 chars
- Status: every discipline = not_started uniformly
- Created/updated: 2026-05-07 (the migration date)
- Priority mapping: P0→mvp, P1→v1.1, P2→v2.0, P3→backlog
- Order: trailing integer of the original ID
- Inherits non_functional_baseline + ui_baseline implicitly (omitted from frontmatter)
- Self-edges in depends_on are dropped (recorded in the migration report)
"""

from __future__ import annotations

import argparse
import re
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import Any, Dict, List, Optional, Tuple

import yaml

REPO_ROOT = Path(__file__).resolve().parents[2]
LEGACY_PATH = REPO_ROOT / "docs" / "requirements.yaml"
REQ_ROOT = REPO_ROOT / "docs" / "requirements"
MIGRATION_DATE = "2026-05-07"

PRIORITY_TO_RELEASE = {"P0": "mvp", "P1": "v1.1", "P2": "v2.0", "P3": "backlog"}

OLD_ID_RE = re.compile(r"^(EPIC|FEAT|STORY)-(\d{1,5})$")


def slugify(title: str, max_len: int = 60) -> str:
    s = title.lower()
    s = re.sub(r"[^a-z0-9]+", "-", s).strip("-")
    if len(s) > max_len:
        s = s[:max_len].rstrip("-")
    return s or "untitled"


def map_id(old: str) -> str:
    m = OLD_ID_RE.match(old)
    if not m:
        raise ValueError(f"unrecognised legacy id: {old}")
    kind = {"EPIC": "epic", "FEAT": "feature", "STORY": "story"}[m.group(1)]
    return f"gw_{kind}_{int(m.group(2)):05d}"


def map_id_list(values: Optional[List[str]]) -> List[str]:
    if not values:
        return []
    return [map_id(v) for v in values]


def order_from_old_id(old: str) -> int:
    m = OLD_ID_RE.match(old)
    if not m:
        return 0
    return int(m.group(2))


def initial_status_block() -> Dict[str, str]:
    return {d: "not_started" for d in ("backend", "frontend", "infra", "docs", "qa")}


def yaml_dump(data: Dict[str, Any]) -> str:
    return yaml.safe_dump(
        data, sort_keys=False, allow_unicode=True, default_flow_style=False, width=2_000_000
    )


def write_node(path: Path, frontmatter: Dict[str, Any], body: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    fm_text = yaml_dump(frontmatter)
    if not body.endswith("\n"):
        body = body + "\n"
    path.write_text(f"---\n{fm_text}---\n{body}", encoding="utf-8")


