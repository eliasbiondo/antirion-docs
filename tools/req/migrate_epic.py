# ---------------------------------------------------------------------------
# Loader
# ---------------------------------------------------------------------------

@dataclass
class Source:
    epics: Dict[str, Dict[str, Any]]
    features: Dict[str, Dict[str, Any]]
    stories_by_feature: Dict[str, List[Dict[str, Any]]]


def load_source() -> Source:
    raw = yaml.safe_load(LEGACY_PATH.read_text(encoding="utf-8"))
    epics = {e["id"]: e for e in raw.get("epics") or []}
    features = {f["id"]: f for f in raw.get("features") or []}
    stories_by_feature: Dict[str, List[Dict[str, Any]]] = {}
    for s in raw.get("stories") or []:
        stories_by_feature.setdefault(s["feature"], []).append(s)
    return Source(epics=epics, features=features, stories_by_feature=stories_by_feature)


# ---------------------------------------------------------------------------
# Migrate one epic
# ---------------------------------------------------------------------------

def _epic_scope_ids(old_epic_id: str, src: Source) -> set:
    """Return the set of new gw_* ids that will exist after migrating this epic.
    Used to decide which depends_on/related/etc. references stay in frontmatter
    and which get exiled to body Notes (cross-epic refs that will be reconnected
    in pass 2)."""
    in_scope: set = {map_id(old_epic_id)}
    epic = src.epics[old_epic_id]
    # Always include the seed policies + glossary (written by _seed_policies.py)
    in_scope.update(f"gw_policy_{i:05d}" for i in range(1, 6))
    in_scope.update(f"gw_glossary_{i:05d}" for i in range(1, 6))
    for old_feat_id in epic.get("features") or []:
        in_scope.add(map_id(old_feat_id))
        for s in src.stories_by_feature.get(old_feat_id) or []:
            in_scope.add(map_id(s["id"]))
    return in_scope


def migrate_epic(old_epic_id: str, src: Source, dry_run: bool = False) -> Tuple[List[Path], List[str]]:
    epic = src.epics.get(old_epic_id)
    if epic is None:
        raise SystemExit(f"epic {old_epic_id} not found in source YAML")
    written: List[Path] = []
    notes: List[str] = []  # migration manual-review notes
    in_scope = _epic_scope_ids(old_epic_id, src)

    epic_id_new = map_id(old_epic_id)
    epic_slug = slugify(epic["title"])
    epic_dir = REQ_ROOT / f"{epic_id_new}_{epic_slug}"

    epic_fm = epic_frontmatter(epic)
    epic_body = build_epic_body(epic)
    epic_path = epic_dir / "EPIC.md"
    if not dry_run:
        write_node(epic_path, epic_fm, epic_body)
    written.append(epic_path)

    for old_feat_id in epic.get("features") or []:
        feature = src.features.get(old_feat_id)
        if feature is None:
            notes.append(f"{old_epic_id}: feature {old_feat_id} listed but not defined in YAML — skipped")
            continue
        feat_id_new = map_id(old_feat_id)
        feat_slug = slugify(feature["title"])
        feat_dir = epic_dir / f"{feat_id_new}_{feat_slug}"
        dropped: List[str] = []
        feat_external: Dict[str, List[str]] = {}
        feat_fm = feature_frontmatter(feature, dropped, in_scope, feat_external)
        feat_body = build_feature_body(feature, feat_external, dropped)
        if dropped:
            notes.append(f"{old_feat_id}: dropped self-edge in depends_on")
        if feat_external:
            notes.append(
                f"{old_feat_id}: deferred external refs to body Notes "
                f"({sum(len(v) for v in feat_external.values())} total)"
            )
        feat_path = feat_dir / "FEATURE.md"
        if not dry_run:
            write_node(feat_path, feat_fm, feat_body)
        written.append(feat_path)

        for story in src.stories_by_feature.get(old_feat_id) or []:
            story_id_new = map_id(story["id"])
            story_slug = slugify(story["title"])
            story_dir = feat_dir / f"{story_id_new}_{story_slug}"
            sdrop: List[str] = []
            sext: Dict[str, List[str]] = {}
            sfm = story_frontmatter(story, sdrop, in_scope, sext)
            sbody = build_story_body(story, sdrop, sext)
            if sdrop:
                notes.append(f"{story['id']}: dropped self-edge in depends_on")
            if sext:
                notes.append(
                    f"{story['id']}: deferred external refs to body Notes "
                    f"({sum(len(v) for v in sext.values())} total)"
                )
            spath = story_dir / "STORY.md"
            if not dry_run:
                write_node(spath, sfm, sbody)
            written.append(spath)
    return written, notes


def main(argv: Optional[List[str]] = None) -> int:
    p = argparse.ArgumentParser()
    p.add_argument("epic", nargs="?", help="epic id (e.g. EPIC-008)")
    p.add_argument("--all", action="store_true", help="migrate every epic")
    p.add_argument("--dry-run", action="store_true")
    args = p.parse_args(argv)

    src = load_source()
    targets: List[str]
    if args.all:
        targets = list(src.epics.keys())
    elif args.epic:
        targets = [args.epic]
    else:
        p.error("supply EPIC-NNN or --all")
        return 2
    all_notes: List[str] = []
    total = 0
    for tid in targets:
        written, notes = migrate_epic(tid, src, dry_run=args.dry_run)
        total += len(written)
        all_notes.extend(notes)
        action = "would write" if args.dry_run else "wrote"
        print(f"{tid}: {action} {len(written)} files")
    if all_notes:
        print("\nManual-review notes:")
        for n in all_notes:
            print(f"  - {n}")
    print(f"\ntotal files: {total}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
